class math:
    @staticmethod
    def add():
        a = 10
        b = 15
        print("Result:", (a+b))
    def sub(self):
        a = 20
        b = 30
        print("Result", (a-b))
    def multiply(self):
        a = 40
        b = 50
        print("Result", (a*b))
    def divide(self):
        a = 200
        b = 4
        print("Result", (a//b))
    def modulus(self):
        a = 3000
        b = 90
        print("Result", (a%b))

u = math()
v = math()
x = math()
y = math()
z = math()

u.add()
v.sub()
x.multiply()
y.divide()
z.modulus()